// Current language
let currentLang = localStorage.getItem('zai-lang') || 'en';

// Track dropdown state
let langDropdownOpen = false;

// Get translation
function __(key) {
    const lang = ZAI_TRANSLATIONS[currentLang] || ZAI_TRANSLATIONS['en'];
    const text = lang[key];
    if (text === undefined) {
        // Fallback to English
        return ZAI_TRANSLATIONS['en'][key] || key;
    }
    return text;
}

// Apply translations to all elements with data-i18n attribute
function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translation = __(key);
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.placeholder = translation;
        } else {
            el.innerHTML = translation;
        }
    });
    
    // Update HTML lang attribute
    document.documentElement.lang = currentLang === 'si' ? 'si' : currentLang === 'ta' ? 'ta' : 'en';
    
    // Update language button text
    const langBtn = document.getElementById('langToggle');
    if (langBtn) {
        const langNames = { 'en': 'EN', 'si': 'සිං', 'ta': 'த' };
        langBtn.textContent = langNames[currentLang] || 'EN';
        langBtn.setAttribute('aria-label', __(`lang.${currentLang}`));
    }
    
    // Update page title
    const titleKey = document.querySelector('title')?.getAttribute('data-i18n');
    if (titleKey) {
        document.title = __(titleKey);
    }
    
    // Update mobile menu links
    document.querySelectorAll('.mobile-menu-link').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (key) {
            el.innerHTML = __(key);
        }
    });
    
    // Update subject cards (they're generated dynamically)
    // This will be handled by re-rendering when needed
    
    // Update stat labels
    document.querySelectorAll('.hero-stat-label').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (key) el.textContent = __(key);
    });
    
    // Special handling for footer copyright
    const footerCopyright = document.querySelector('.footer-bottom p strong');
    if (footerCopyright) {
        const parent = footerCopyright.closest('p');
        if (parent) {
            const key = parent.getAttribute('data-i18n');
            if (key) parent.innerHTML = __(key);
        }
    }
}

// Switch language
function switchLanguage(lang) {
    if (!ZAI_TRANSLATIONS[lang]) return;
    currentLang = lang;
    localStorage.setItem('zai-lang', lang);
    applyTranslations();
    
    // Show toast
    const langNames = { 'en': 'English 🇬🇧', 'si': 'සිංහල 🇱🇰', 'ta': 'தமிழ் 🇱🇰' };
    showToast(`🌐 Language changed to ${langNames[lang]}`, 'success');
}

// Initialize language
function initLanguage() {
    applyTranslations();
    
    // Language toggle button - toggle dropdown on click
    const langBtn = document.getElementById('langToggle');
    const langDropdown = document.getElementById('langDropdown');
    
    if (langBtn) {
        langBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            if (langDropdown) {
                langDropdownOpen = !langDropdownOpen;
                langDropdown.classList.toggle('open', langDropdownOpen);
            }
        });
    }
    
    // Language dropdown items
    document.querySelectorAll('[data-lang]').forEach(el => {
        el.addEventListener('click', () => {
            const lang = el.getAttribute('data-lang');
            switchLanguage(lang);
            // Close dropdown after selection
            if (langDropdown) {
                langDropdown.classList.remove('open');
                langDropdownOpen = false;
            }
        });
    });
    
    // Close dropdown when clicking outside
    document.addEventListener('click', (e) => {
        if (langDropdownOpen && langBtn && langDropdown) {
            if (!langBtn.contains(e.target) && !langDropdown.contains(e.target)) {
                langDropdown.classList.remove('open');
                langDropdownOpen = false;
            }
        }
    });
}

// Cycle through languages: English → Sinhala → Tamil → English
function cycleLanguage() {
    const langs = ['en', 'si', 'ta'];
    const currentIndex = langs.indexOf(currentLang);
    const nextLang = langs[(currentIndex + 1) % langs.length];
    switchLanguage(nextLang);
}
